# ─── normalize_units (legacy wrapper) ────────────────────────────────────────

def test_normalize_units_filters_banner_row_from_legacy_extractor_output():
    # The legacy openpyxl parser returns a list of pre-mapped dicts. Verify
    # that the "Current/Notice/Vacant Residents" banner is still filtered.
    units = [
        {"unit_number": "Current/Notice/Vacant Residents",
         "resident_name": None, "market_rent": None, "in_place_rent": None},
        {"unit_number": "101", "unit_type": "A1", "sqft": 800,
         "resident_name": "Jane Doe", "status": "Occupied",
         "market_rent": 1200, "in_place_rent": 1150,
         "lease_start": "2025-01-01", "lease_end": "2026-01-01"},
        {"unit_number": "Total", "resident_name": None,
         "market_rent": 1200, "in_place_rent": 1150},
    ]
    result = normalize_units(units, {"unit_number": "Unit"})
    assert len(result.units) == 1
    assert result.units[0]["unit_number"] == "101"
    assert len(result.skipped_rows) == 2


def test_normalize_units_truncates_oversize_unit_number():
    """Regression: the production StringDataRightTruncation error came from
    shoving 'Current/Notice/Vacant Residents' (30 chars) into VARCHAR(20).
    After the migration -> VARCHAR(50), a short banner like this still gets
    filtered as junk. But if someone constructs a real unit_number > 50,
    we truncate-with-warning rather than crash.
    """
    units = [
        {"unit_number": "A" * 120, "unit_type": "B2",
         "resident_name": "Jane Doe", "status": "Occupied",
         "market_rent": 1500, "in_place_rent": 1450},
    ]
    result = normalize_units(units)
    assert len(result.units) == 1
    assert len(result.units[0]["unit_number"]) == 50
    assert any("truncated" in w for w in result.warnings)


