"""Tests for rent_roll_normalizer.

Covers the eight assertions from the task spec:
  1. "Current/Notice/Vacant Residents" banner row is filtered as junk
     with a reason.
  2. Total/Subtotal rows are filtered.
  3. All real units parse when fed a clean table.
  4. Header detection finds the correct row even when preceded by 5+
     metadata rows.
  5. Column aliasing works — `"Apt #"` instead of `"Unit"` still maps.
  6. VARCHAR overflow doesn't crash — truncated with a warning.
  7. Unmapped columns appear in `unmapped_columns`, not silently dropped.
  8. Missing/malformed dates coerce to None, don't crash.
  9. Currency with `$`, `,`, `()` parses correctly.
"""
from datetime import datetime
