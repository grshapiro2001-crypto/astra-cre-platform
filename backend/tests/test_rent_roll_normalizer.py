    assert len(result.units[0]["unit_type"]) == COLUMN_MAX_LENGTHS["unit_type"]
    assert len(result.units[0]["resident_name"]) == COLUMN_MAX_LENGTHS["resident_name"]
    assert any("truncated" in w for w in result.warnings)


